export function countEmoji(count) {
    if (count < 5) {
        return '😆';
    }
    if (count < 10) {
        return '😄';
    }
    if (count < 15) {
        return '😃';
    }
    if (count < 20) {
        return '😀';
    }
    if (count < 25) {
        return '🙂';
    }
    if (count < 30) {
        return '😉';
    }
    if (count < 35) {
        return '😌';
    }
    if (count < 40) {
        return '🧐';
    }
    if (count < 45) {
        return '🤪';
    }
    if (count < 50) {
        return '😢';
    }
    if (count < 55) {
        return '😫';
    }
    if (count < 60) {
        return '🤯';
    }
    if (count < 65) {
        return '😶‍🌫️';
    }
    if (count < 70) {
        return '🫠';
    }
    if (count < 75) {
        return '🤢';
    }
    if (count < 80) {
        return '👨🏻‍🦽';
    }
    if (count < 85) {
        return '😈';
    }
    if (count < 90) {
        return '💀';
    }
    if (count < 95) {
        return '🧟‍♂️';
    }
    if (count < 100) {
        return '🧞‍♂️';
    }
    return '😇'
}